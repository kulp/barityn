public class Compare {
    public static boolean lessThan(float a, float b) {
        return a < b;
    }

    public static boolean moreThan(float a, float b) {
        return a > b;
    }

    public static boolean lessThan(double a, double b) {
        return a < b;
    }

    public static boolean moreThan(double a, double b) {
        return a > b;
    }

    public static boolean lessThan(long a, long b) {
        return a < b;
    }

    public static boolean moreThan(long a, long b) {
        return a > b;
    }
}
